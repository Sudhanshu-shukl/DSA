# i => 0->n-1 
# formula -> [i,2*(n-i-1))+1,i]

n = 3

for i in range(n): # 0 1 2
    for j in range(i): # none then 0(1 times space) then 1(2 times space)
        print("  ",end="")
    for j in range((2*(n-i-1))+1):
        print("*",end=" ")
    print()
    
    

